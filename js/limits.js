/**
 * Aplica los colores y ratios al teclado basándose en el Grado seleccionado
 * @param {string|number} degreeId - El ID del grado seleccionado (0-42)
 */
function applyLimitColors(degreeId) {
    const degree = parseInt(degreeId);
    const mapping = degreeMappings[degree];

    if (!mapping) {
        console.warn(`No mapping found for Grade ${degree}`);
        return;
    }

    // Iteramos sobre todas las teclas del teclado hexagonal
    keyConfigurations.forEach(config => {
        const noteIndex = parseInt(config.text);

        // Verificamos si tenemos un límite definido para esta nota
        if (mapping.hasOwnProperty(noteIndex)) {
            const data = mapping[noteIndex];
            const newColor = limitColors[data.limit];
            const newRatio = data.ratio;

            if (newColor) {
                updateKeyVisuals(config.id, newColor, newRatio);
            }
        }
    });

    // Indicar visualmente que estamos en un modo de límite (opcional)
    document.body.classList.add('limits-active');
}

